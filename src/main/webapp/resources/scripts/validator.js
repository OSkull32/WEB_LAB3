function validateXValue(x) {
    let isXCorrect = false;

    if ((x>=-4) && (x<=4)) {
        isXCorrect=true;
    }
    return isXCorrect;
}

function validateYValue(y) {
    let isYCorrect = false;

    if ((y>=-5) && (y<=5)) {
        isYCorrect=true;
    }
    return isYCorrect;
}
function validateRValue(r) {
    let isRCorrect = false;

    if ((r>=2) && (r<=5)) {
        isRCorrect=true;
    }
    return isRCorrect;
}
